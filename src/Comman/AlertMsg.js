import 'react-toastify/dist/ReactToastify.css';
import { toast } from 'react-toastify';


export const toastifySuccess = (message) => {
    toast.success(`${message}`, {
        position: "top-right",
        autoClose: 1000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
    });
}

